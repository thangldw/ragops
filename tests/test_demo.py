import json
import os
from pathlib import Path

import pytest

from ragops.cli import main
from ragops.demo import write_demo


def test_demo_writes_blocked_candidate_and_reviewable_reports(tmp_path: Path) -> None:
    summary = write_demo(tmp_path / "demo")

    assert summary["demo_completed"] is True
    assert summary["scenario_id"] == "japanese-troubleshooting"
    assert summary["candidate_decision"] == "BLOCK"
    assert "citation_coverage_regression" in summary["failed_gates"]
    html = (tmp_path / "demo" / "release-report.html").read_text(encoding="utf-8")
    assert "RAGOps regression report" in html
    assert "Generated by" in html
    assert "https://github.com/thangldw/ragops" in html


def test_demo_cli_returns_success_for_expected_block(monkeypatch, capsys, tmp_path: Path) -> None:
    output = tmp_path / "quickstart"
    monkeypatch.setattr("sys.argv", ["ragops", "demo", "--output", str(output)])

    assert main() == 0
    summary = json.loads(capsys.readouterr().out)
    assert summary["candidate_decision"] == "BLOCK"
    assert (output / "release-report.md").is_file()


