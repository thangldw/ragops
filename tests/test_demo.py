def test_demo_rejects_unknown_scenario_before_writing(tmp_path: Path) -> None:
    output = tmp_path / "unknown"
    with pytest.raises(ValueError, match="unknown demo scenario"):
        write_demo(output, scenario_id="unknown")
    assert not output.exists()


def test_demo_refuses_existing_output_without_force(tmp_path: Path) -> None:
    output = tmp_path / "demo"
    output.mkdir()

    with pytest.raises(FileExistsError, match="already exists"):
        write_demo(output)


def test_demo_force_replaces_regular_files(tmp_path: Path) -> None:
    output = tmp_path / "demo"
    output.mkdir()
    (output / "release-report.md").write_text("old", encoding="utf-8")

    summary = write_demo(output, force=True)

    assert summary["candidate_decision"] == "BLOCK"
    assert (output / "release-report.md").read_text(encoding="utf-8").startswith(
        "# RAGOps regression check"
    )


@pytest.mark.skipif(not hasattr(os, "symlink"), reason="symlinks are unavailable")
def test_demo_refuses_symlinked_output_directory(tmp_path: Path) -> None:
    real_output = tmp_path / "real"
    real_output.mkdir()
    linked_output = tmp_path / "linked"
    linked_output.symlink_to(real_output, target_is_directory=True)

    with pytest.raises(FileExistsError, match="symlinked demo output directory"):
        write_demo(linked_output, force=True)


@pytest.mark.skipif(not hasattr(os, "symlink"), reason="symlinks are unavailable")
def test_demo_refuses_symlinked_child_without_overwriting_target(tmp_path: Path) -> None:
    output = tmp_path / "demo"
    output.mkdir()
    victim = tmp_path / "victim.md"
    victim.write_text("SAFE", encoding="utf-8")
    (output / "release-report.md").symlink_to(victim)

    with pytest.raises(FileExistsError, match="symlinked demo output file"):
        write_demo(output, force=True)

    assert victim.read_text(encoding="utf-8") == "SAFE"
